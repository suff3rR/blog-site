def get_date(post):
    return post['date']


# Create your views here.
def index(request):    #starting page 
    latest_posts = sorted(all_posts , key=get_date)
    sorted_posts = latest_posts[-3:]
    return render(request,"posts/homepage.html",{
        "posts" : sorted_posts
    })

def posts(request):    #display all posts
    return render(request,"posts/all_posts.html",{
        "all_posts" : all_posts
    })

def post_details(request , slug): # Individual post logic
    post = next(post for post in all_posts if post['slug'] == slug)  #return the post from all_posts that matches the if condition 
    return render(request, "posts/post-detail.html", {
        "post" : post
    })